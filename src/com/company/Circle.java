package com.company;

public class Circle {

    public static void main(String[] args) {
        double radius=3;
        double pi=3.14;
        double area=pi*radius*radius;
        double perimeter=2*pi*radius;

        System.out.println("Area is -> " +  area);
        System.out.println("Perimeter is -> " + perimeter);
    }
}

